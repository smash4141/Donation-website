	<h3>Need of Waste Management System?</h3>
<p>Solid waste management affects every single person in the world, whether individuals are managing their own waste or governments are providing waste management services to their citizens. As nations and cities urbanize, develop economically, and grow in terms of population, the World Bank estimates that waste generation will increase from 2.01 billion tonnes in 2016 to 3.40 billion tonnes in 2050. At least 33% of this waste is mismanaged globally today through open dumping or burning.<br>
<u><b>Global problems:</b></u><br>
Rich countries (such as the United States, Canada and members of the European Union) represent just 16% of global population, but produce 34% of the world’s waste. An estimated 93% of waste in low-income countries is mismanaged, as compared to 2% in high-income countries. <br><br>

Daily per capita waste generation is estimated to be 4.87 pounds in North America, as compared to 1.01 pounds in Sub-Saharan Africa. Though the World Bank expects waste to triple in Sub-Saharan Africa and to double in South Asia, accounting for a full 35% of global output by 2050.<br><br>
Collection is more common in urban areas for these low-income countries than in rural areas, but is still far less prevalent than in developed nations. The World Bank found that the amount of collection in low-income countries increased from about 22% to 39% since 2012 — though noted data may not be directly comparable
	</p>
	<p>
<u><b>Climate issues</b></u>
Global waste production accounted for about 5% of global emissions in 2016, with an output of 1.6 billion metric tons of carbon-dioxide-equivalent. That number is expected to grow to 2.6 billion billion metric tons by 2050.<br><br>

Food waste accounted for 47% of those emissions — showing both a monumental problem and an opportunity for gases released by food waste to be harnessed for energy. Silpa Kaza, the report's lead author, told Waste Dive these emissions "are mostly driven by open dumping of waste and then landfilling of waste without landfill gas systems."<br><br>

Underdeveloped waste management infrastructure may also become a growing sign of global inequity as climate change advances. The open dumpsites in poorer countries can pose multiple hazards in the form of deadly landslides — which may be exacerbated by weather events — or pollution in waterways. Because of this, Kaza said a case can be made for addressing waste as part of broader climate resiliency strategies.
	</p>
